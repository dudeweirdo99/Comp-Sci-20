package Exercise3;

public class Vehicle {
	
	private boolean vRuns;
	private int vYear;
	private String vName;
	private boolean vGas, vHybrid;
	
	public Vehicle(int year, String name, boolean runs, boolean gas, boolean hybrid) {
		vRuns = runs;
		vYear = year;
		vName = name;
		vGas = gas;
		vHybrid = hybrid;
	}
	
	public boolean getRuns() {
		return(vRuns);
	}
	
	public int getYear() {
		return(vYear);
	}
	
	public String getName() {
		return(vName);
	}
	
	public boolean getGas() {
		return(vGas);
	}
	
	public boolean getHybrid() {
		return(vHybrid);
	}
}
